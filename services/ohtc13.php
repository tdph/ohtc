            <p class="description">
                This course is designed to enhance the knowledge, skills and attitude of students to achieve the competencies required of SHIP’S CATERING SERVICES NC II in accordance with industry standards.  It covers specialized competencies on performing maintenance and sanitation of galley equipment and facility, preparing and cooking meat dishes, preparing stocks, sauces and soups, preparing appetizers, salads and sandwiches (Hot and Cold and Open), preparing side dishes and breakfast meal, preparing and cooking poultry products, preparing and cooking seafood, preparing bread products, hot and cold desserts and performing stock control.
                This intensive course is compliant to the guidelines set by the Maritime Labor Convention (MLC) 2006.
                All course participants will be well prepared to pass the final Assessment and qualification as fully qualified Ships Cook.
