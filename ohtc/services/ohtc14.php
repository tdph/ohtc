            <p class="description">
                This course is designed to equip individual with skills, knowledge and attitudes required in rendering catering services in accordance with industry standards. It covers core competencies in providing mess hall services; perform housekeeping services as well as providing assistance in receiving and storing provision and supplies.
                A person who has completed this training may be qualified to become a Messman/Steward.
                This intensive course is compliant to the guidelines set by the Maritime Labor Convention (MLC) 2006.
            </p>
            <h4>Duration</h4>
            <p>5 days or 50 hours intensive course</p>
            <h4>The Units of Competency include the following:</h4>
            <h4>Basic Competencies</h4>
            <ul>
                <li>Receive and respond  to workplace communication Work in team environment</li>
                <li>Work with others</li>
                <li>Demonstrate work values</li>
                <li>Practice housekeeping procedures</li>
            </ul>
            <h4>Common Competencies</h4>
            <ul>
                <li>Observe Personal Hygiene</li>
                <li>Practice Food Safety, Sanitation and Hygiene. Including HACCP principles.</li>
                <li>Observe Catering Health and Safety Practices</li>
                <li>Protect Marine Environment/Waste Segregation Management</li>
                <li>Work within Multi-cultural and Religious Environment</li>
            </ul>
            <h4>Core Competencies</h4>
            <ul>
                <li>Perform mess hall service</li>
                <li>Perform housekeeping services</li>
                <li>Provide assistance in receiving and storing provision and supplies</li>
                <li>Prepare side dishes and breakfast meals</li>
            </ul>
